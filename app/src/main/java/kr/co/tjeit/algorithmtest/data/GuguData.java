public class GuguData {
    private int dan;
    private int multiply;

    public GuguData() {

    }

    public GuguData(int dan, int multiply) {
        this.dan = dan;
        this.multiply = multiply;
    }

    public int getDan() {
        return dan;
    }

    public void setDan(int dan) {
        this.dan = dan;
    }

    public int getMultiply() {
        return multiply;
    }

    public void setMultiply(int multiply) {
        this.multiply = multiply;
    }
}
